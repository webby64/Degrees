def shortest_path(source, target):
    """
    Returns the shortest list of (movie_id, person_id) pairs
    that connect the source to the target.

    If no possible path, returns None.
    """

    # TODO
    if source == target:
        return None
    visited = set()
    previous = {}
    stack = [source]
    while stack:
        temp = []
        for s in stack:  # for the immediate sources
            if s not in visited:
                visited.add(s)
                for neightup in neighbors_for_person(s):
                    if neightup[1] == target:  # if we reach the target
                        lis = [neightup]
                        while s != source:
                            data = previous[s]
                            lis.append(data[0])
                            s = data[1]

                        lis.reverse()
                        return lis
                    else:
                        if neightup[1] not in previous:
                            previous[neightup[1]] = (neightup,s)
                            temp.append(neightup[1])
            else:
                continue
        stack = temp
    return None




def person_id_for_name(name):
    """
    Returns the IMDB id for a person's name,
    resolving ambiguities as needed.
    """
    person_ids = list(names.get(name.lower(), set()))
    if len(person_ids) == 0:
        return None
    elif len(person_ids) > 1:
        print("Which '{}'?".format(name))
        for person_id in person_ids:
            person = people[person_id]
            name = person["name"]
            birth = person["birth"]
            print("ID: {}, Name: {}, Birth: {}".format(person_id,name,birth))
        try:
            person_id = input("Intended Person ID: ")
            if person_id in person_ids:
                return person_id
        except ValueError:
            pass
        return None
    else:
        return person_ids[0]


def neighbors_for_person(person_id):
    """
    Returns (movie_id, person_id) pairs for people
    who starred with a given person.
    """

    movie_ids = people[person_id]["movies"]
    neighbors = set()
    for movie_id in movie_ids:
        for person_id in movies[movie_id]["stars"]:
            neighbors.add((movie_id, person_id))
    return neighbors


